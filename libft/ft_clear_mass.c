#include "libft.h"

void	ft_clear_mass(char ***mas)
{
	char	**s;

	s = *mas;
	while (*s)
	{
		if (*s)
		{
			free(*s);
			*s = NULL;
		}
		s++;
	}
	free(*mas);
}
